var arr = ["este é o primeiro elemento", "este é o segundo elemento"];
console.log(arr[0]); // exibe 'este é o primeiro elemento'
console.log(arr[1]); // exibe 'este é o segundo elemento'
console.log(arr[arr.length - 1]); // exibe 'este e o segundo elemento'
//O primeiro elemento de um array está na posição zero, e o 'último elemento' está elevado ao valor, menos um 

